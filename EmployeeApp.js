class Employee{

    constructor(){
        id= Number;
        Name = String;
        department= String;
        salary= Number;
    }

    getID(){
        return this.id;
    }

    getname(){
        return this.Name;
    }

    getdepartment(){
        return this.department;
    }

    getsalary(){
        return this.salary;
    }
}
